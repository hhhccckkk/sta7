package com.mi.sta7.bean;

public class HistoryBean {
	private String history_url;
	private String history_name;
	private String history_no;
	public String getHistory_url() {
		return history_url;
	}
	public void setHistory_url(String history_url) {
		this.history_url = history_url;
	}
	public String getHistory_name() {
		return history_name;
	}
	public void setHistory_name(String history_name) {
		this.history_name = history_name;
	}
	public String getHistory_no() {
		return history_no;
	}
	public void setHistory_no(String history_no) {
		this.history_no = history_no;
	}
}
